def rps(choice):
    """سنگ کاغذ قیچی"""
    options = ["سنگ 🪨", "کاغذ 📄", "قیچی ✂️"]
    bot_choice = random.choice(options)

    wins = {
        "سنگ": "قیچی",
        "کاغذ": "سنگ",
        "قیچی": "کاغذ"
    }

    user_key = None
    for k in wins:
        if k in choice:
            user_key = k
            break

    if not user_key:
        return "❓ گزینه معتبر نیست. بنویس: سنگ، کاغذ یا قیچی"

    bot_key = bot_choice.split()[0]

    if user_key == bot_key:
        result = "🤝 مساوی!"
    elif wins[user_key] == bot_key:
        result = "🎉 بردی!"
    else:
        result = "😅 باختی!"

    return f"تو: {choice}\nمن: {bot_choice}\n\n{result}"


